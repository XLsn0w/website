function getPlifilename()
	local sysver = getOSVer()
	local sysint = tonumber(string.sub(sysver, 1, 1)..string.sub(sysver, 3,3)..string.sub(sysver, 5, 5))
	local path=appDataPath('com.madhead.tos.zh')
	local plfilename
	if #sysver<4 then sysver=sysver..'.0' end --增位
	local newpath=string.sub(appBundlePath('com.madhead.tos.zh'),1,string.find(appBundlePath('com.madhead.tos.zh'),"/z"))
	if 
		sysint >=800  	--是8系统！
	then
		plfilename=path..'/Library/Preferences/com.madhead.tos.zh.plist' 
	else 
		plfilename=newpath..'Library/Preferences/com.madhead.tos.zh.plist' 
	end
	return plfilename
end
function addLabel(tb,text,size,align)
    local align=align or 'left'
	local size=size or 18
	local tmp={
            ["type"] = "Label",
            ["text"] = text,
            ["size"] = size,
            ["align"] = align,
            ["color"] = "0,0,255",
        }
	table.insert(tb,tmp)
end
function addImage(tb,id)
	local path
	if id <10 then path='60px-00'..id..'i.png' end
	if id>=10 and id<100 then path='60px-0'..id..'i.png' end
	if id>=100 then path='60px-'..id..'i.png' end
	local tmp={
		 ["type"] = "Image",
		 ["src"]  = path 
		
		 }
		table.insert(tb,tmp)
end
